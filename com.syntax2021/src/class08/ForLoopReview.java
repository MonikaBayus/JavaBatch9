package class08;

public class ForLoopReview {

	public static void main(String[] args) {
		
	int num1 = 1;
	int mult;
	
	for(int i = 1; i<=10; i++) {
		mult=num1*i;
		System.out.println(num1 + "*" + i + " = " + mult);
	}
	
	
System.out.println("----------------------------------------------------------");

for(int i=1;i<=10;i++) {
	int num=3;
	num*=i;
	System.out.println(num);
}
			

	}

}


