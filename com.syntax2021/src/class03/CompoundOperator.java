package class03;

public class CompoundOperator {

	public static void main(String[] args) {
	 
		int num1=500;
		num1+=500;
		num1-=67;
		System.out.println(num1);
		
		int cakePiece=11;
		cakePiece/=4;
		
		System.out.println(cakePiece);
		
		double cake=25;
		cake/=7;
		cake%=7;
		
		System.out.println(cake);
		
		
	}

}
