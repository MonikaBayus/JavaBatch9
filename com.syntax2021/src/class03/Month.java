public class Month {

	public static void main(String[] args) {

		int name1 = 7;

		if (name1 == 1) {
			System.out.println("Jan");

		} else if (name1 == 2) {
			System.out.println("Feb");
		} else if (name1 == 3) {
			System.out.println("Mar");
		} else if (name1 == 4) {
			System.out.println("April");
		} else if (name1 == 5) {
			System.out.println("May");
		} else if (name1 == 6) {
			System.out.println("Jun");
		} else {
			System.out.println("none");

		}

	}

}

